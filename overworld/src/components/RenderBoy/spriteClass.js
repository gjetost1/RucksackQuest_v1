import globalvars from "../../globalvars"

class Sprite {
  constructor({ position, velocity, image }) {
    this.position = position
    this.image = image
  }
  draw(){
    globalvars.ctx.drawImage(this.image, this.position.x, this.position.y, globalvars.scene_width, globalvars.scene_height)
  }}

  export default Sprite
